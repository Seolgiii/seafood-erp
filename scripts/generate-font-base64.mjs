/**
 * public/fonts/*.otf → lib/fonts-base64.ts 자동 생성 스크립트
 * package.json의 "prebuild"에 등록되어 next build 전에 자동 실행됩니다.
 * lib/fonts-base64.ts는 생성 파일이므로 .gitignore에 포함됩니다.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

function toDataUri(filename) {
  const filePath = path.join(root, "public", "fonts", filename);
  const buf = fs.readFileSync(filePath);
  return `data:font/otf;base64,${buf.toString("base64")}`;
}

const regular = toDataUri("NotoSansKR-Regular.otf");
const bold = toDataUri("NotoSansKR-Bold.otf");

const output = `// AUTO-GENERATED by scripts/generate-font-base64.mjs — DO NOT EDIT
// Regenerated automatically before each build via "prebuild" script.

export const NOTO_SANS_KR_REGULAR = "${regular}";

export const NOTO_SANS_KR_BOLD = "${bold}";
`;

const outPath = path.join(root, "lib", "fonts-base64.ts");
fs.writeFileSync(outPath, output, "utf-8");

const kb = Math.round(fs.statSync(outPath).size / 1024);
console.log(`[generate-font-base64] lib/fonts-base64.ts 생성 완료 (${kb} KB)`);
